# app/prompts/interview_qas_prompts.py

def generate_interview_qas_prompt(pplx_content: str, resume_content: str) -> str:
    return f"""
Perplexity 검색 내용: {pplx_content}
자기소개서 내용: {resume_content}

지원하고자 하는 기업 및 직무에 대한 깊이 있는 면접 질문을 생성하기 위해, 아래 지침과 구조를 따라 질문을 생성해주세요.

# Steps

1. 기업 및 직무 이해: Perplexity에서 가져온 기업 및 직무에 대한 정보를 분석합니다. 
2. 자기소개서 분석: 자기소개서의 주요 내용, 특히 개인의 강점, 경험, 성과를 식별합니다.
3. 경험 연결: 지원자의 경험과 역량을 평가할 수 있는 질문을 생성합니다.
4. 교차분석: 자기소개서에서 언급된 강점 및 경험이 기업과 직무에 어떻게 부합하는지 평가합니다.
5. 질문 생성: 위의 분석을 기반으로 깊이 있는 면접 질문을 작성합니다. 질문은 자발적인 대화를 유도하고, 가치와 경험의 연관성을 탐구하며, 지원자의 문제 해결 능력을 평가하는 방향으로 설정합니다.
6. 문법 및 어투: 문법적으로 올바르고 자연스러운 어투로 작성합니다.
7. 중복 방지: 중복되지 않는 질문을 생성합니다.

# Output Format
1. 면접 질문은 1~2 문장으로 구성되며, 각 질문의 의도와 관련 내용을 포함하여 작성합니다.
2. 읽기 쉽고 명확한 문장 구조를 사용합니다.
3. 오직 질문만 출력하세요. 앞서 분석한 내용은 출력하지 않습니다.
4. 질문은 최대 3개까지만 작성해주세요.
5. 각 질문은 1~2 문장으로 구성하고, 줄바꿈 없이 출력하세요.

# Notes
- 각 질문은 지원자의 깊이 있는 사고와 기업 가치와의 일치도를 평가할 수 있어야 합니다.
- 질문 작성 시 구체적인 예시 및 자기소개서의 구체적 경험를 포함하도록 합니다.
- 실제 기업명과 직무는 [기업 A], [직무명]으로 대체되어 있습니다. 원하는 기업과 직무명을 대입하여 사용하세요.
"""
