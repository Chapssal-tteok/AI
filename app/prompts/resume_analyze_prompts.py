# app/prompts/resume_analyze_prompts.py

def generate_resume_analysis_prompt(question: str, resume: str, company: str, position: str, company_summary: str) -> str:
    return f"""
다음은 {company}의 {position} 직무에 지원한 자기소개서 문항과 답변입니다.

지원 기업 요약 정보 (Perplexity 기반):
{company_summary}

[자기소개서 문항]
{question}

[지원자의 답변]
{resume}

이 자기소개서 문항에 대한 답변을 다음 기준에 따라 평가하고 구체적인 피드백을 작성해주세요.

# 평가 기준
1. **질문 충실도**: 질문에서 요구하는 내용을 정확히 이해하고 충실히 답했는지
2. **논리적인 흐름**: 답변이 구조적으로 자연스럽고 이해하기 쉬운지
3. **구체성**: 모호하지 않고 구체적인 사례나 수치 등을 통해 설득력을 높였는지
4. **명확성 및 전문성**: 문장이 명확하고, 직무 관련 역량과 용어가 잘 드러났는지
5. **문법 및 어휘**: 어법이 자연스럽고 문법적인 오류가 없는지
6. **기업과 직무 연관성**: Perplexity 기반 기업 요약 정보를 반영했을 때, 기업이 중요하게 여기는 가치나 인재상과 잘 맞는지

# 피드백 작성 가이드
- 각 항목마다 피드백을 1~2문장씩 작성해주세요.
- 긍정적인 부분과 개선할 점을 함께 포함해주세요.
- 문장은 모두 "~요" 형태로 마무리해주세요.
- 마지막에 **전체 종합 피드백**을 2~3문장으로 제공해주세요.
- 출력 시 각 항목 제목은 **볼드체**로 표시해주세요.
"""
